# Operator that divides the two digits and outputs the remainder of the expression

# Check if a number is even or odd

print("I can deduce that the whole number you input is even or odd!")
user_number = int(input("What is your whole number?\n"))

if (user_number % 2 == 0):
    print("Seems like your number is even.")
else:
    print("Yup, you've got an odd number there.")